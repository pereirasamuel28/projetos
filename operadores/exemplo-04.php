<?php

//OPERADORES DE COMPARACAO

$a = 30;
$b = 55;

var_dump($a > $b); //MAIOR

echo "<br>";

var_dump($a < $b); //MENOR

echo "<br>";

var_dump($a == $b); //COMPARA SE O VALOR E IGUAL

echo "<br>";

var_dump($a === $b); //COMPARA SE O TIPO E IGUAL

echo "<br>";

var_dump($a != $b);  //COMPARA SE O VALOR E DIFERENTE

echo "<br>";

var_dump($a !== $b);  //COMPARA SE O TIPO E DIFERENTE
?>